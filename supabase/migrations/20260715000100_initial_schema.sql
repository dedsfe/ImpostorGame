  where is_active;
create index if not exists idx_content_items_selection
  on public.content_items(game_id, category_id, difficulty_id, sort_order)
  where is_active;
create index if not exists idx_content_items_normalized_label
  on public.content_items(normalized_label);
create index if not exists idx_sessions_game_status
  on public.sessions(game_id, status);
create index if not exists idx_sessions_owner
  on public.sessions(owner_user_id)
  where owner_user_id is not null;
create index if not exists idx_session_players_session
  on public.session_players(session_id, display_order);
create index if not exists idx_session_players_user
  on public.session_players(user_id)
  where user_id is not null;
create index if not exists idx_session_assignments_session
  on public.session_assignments(session_id);
create index if not exists idx_session_assignments_player
  on public.session_assignments(session_player_id)
  where session_player_id is not null;

