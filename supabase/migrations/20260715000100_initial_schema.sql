begin;

create or replace function public.set_updated_at()
returns trigger
language plpgsql
set search_path = ''
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

revoke execute on function public.set_updated_at() from public, anon, authenticated;

create table if not exists public.games (
  id bigint generated by default as identity primary key,
  slug text not null unique,
  name text not null,
  short_description text not null,
  setup_screen text not null,
  card_image_path text,
  modal_image_path text,
  min_players integer,
  max_players integer,
  supports_categories boolean not null default false,
  supports_difficulties boolean not null default false,
  supports_timer boolean not null default false,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint games_slug_format check (slug ~ '^[a-z0-9]+(?:-[a-z0-9]+)*$'),
  constraint games_min_players_positive check (min_players is null or min_players > 0),
  constraint games_max_players_valid check (
    max_players is null
    or (max_players > 0 and (min_players is null or max_players >= min_players))
  )
);

create table if not exists public.game_tutorials (
  id bigint generated by default as identity primary key,
  game_id bigint not null unique references public.games(id) on delete cascade,
  title text not null,
  copy text not null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.tutorial_steps (
  id bigint generated by default as identity primary key,
  tutorial_id bigint not null references public.game_tutorials(id) on delete cascade,
  step_order integer not null,
  title text not null,
  copy text not null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint tutorial_steps_order_positive check (step_order > 0),
  unique (tutorial_id, step_order)
);

create table if not exists public.role_templates (
  id bigint generated by default as identity primary key,
  game_id bigint not null references public.games(id) on delete cascade,
  slug text not null,
  name text not null,
  badge text,
  title text not null,
  description text not null,
  tone text not null,
  sort_order integer not null default 0,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint role_templates_slug_format check (slug ~ '^[a-z0-9]+(?:-[a-z0-9]+)*$'),
  constraint role_templates_sort_order_nonnegative check (sort_order >= 0),
  unique (game_id, slug),
  unique (id, game_id)
);

create table if not exists public.categories (
  id bigint generated by default as identity primary key,
  game_id bigint not null references public.games(id) on delete cascade,
  slug text not null,
  name text not null,
  content_kind text not null,
  sort_order integer not null default 0,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint categories_slug_format check (slug ~ '^[a-z0-9]+(?:-[a-z0-9]+)*$'),
  constraint categories_content_kind check (
    content_kind in ('secret_word', 'character', 'mimica_word')
  ),
  constraint categories_sort_order_nonnegative check (sort_order >= 0),
  unique (game_id, slug),
  unique (id, game_id)
);

create table if not exists public.difficulties (
  id bigint generated by default as identity primary key,
  game_id bigint not null references public.games(id) on delete cascade,
  slug text not null,
  name text not null,
  sort_order integer not null default 0,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint difficulties_slug_format check (slug ~ '^[a-z0-9]+(?:-[a-z0-9]+)*$'),
  constraint difficulties_sort_order_nonnegative check (sort_order >= 0),
  unique (game_id, slug),
  unique (id, game_id)
);

create table if not exists public.content_items (
  id bigint generated by default as identity primary key,
  game_id bigint not null references public.games(id) on delete cascade,
  category_id bigint,
  difficulty_id bigint,
  content_kind text not null,
  label text not null,
  normalized_label text not null,
  source_title text,
  source_studio text,
  metadata_json jsonb,
  sort_order integer not null default 0,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint content_items_category_same_game
    foreign key (category_id, game_id)
    references public.categories(id, game_id)
    on delete restrict,
  constraint content_items_difficulty_same_game
    foreign key (difficulty_id, game_id)
    references public.difficulties(id, game_id)
    on delete restrict,
  constraint content_items_kind check (
    content_kind in ('secret_word', 'character', 'mimica_word')
  ),
  constraint content_items_label_not_blank check (btrim(label) <> ''),
  constraint content_items_normalized_label_not_blank check (btrim(normalized_label) <> ''),
  constraint content_items_sort_order_nonnegative check (sort_order >= 0)
);

create table if not exists public.sessions (
  id uuid primary key default gen_random_uuid(),
  game_id bigint not null references public.games(id) on delete restrict,
  owner_user_id uuid references auth.users(id) on delete set null,
  session_code text unique,
  status text not null default 'draft',
  settings_json jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint sessions_status check (status in ('draft', 'active', 'finished', 'cancelled')),
  constraint sessions_code_format check (
    session_code is null or session_code ~ '^[A-Z0-9]{6,12}$'
  )
);

create table if not exists public.session_players (
  id uuid primary key default gen_random_uuid(),
  session_id uuid not null references public.sessions(id) on delete cascade,
  user_id uuid references auth.users(id) on delete set null,
  display_order integer not null,
  nickname text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint session_players_order_nonnegative check (display_order >= 0),
  constraint session_players_nickname_not_blank check (
    nickname is null or btrim(nickname) <> ''
  ),
  unique (session_id, display_order)
);

create table if not exists public.session_assignments (
  id uuid primary key default gen_random_uuid(),
  session_id uuid not null references public.sessions(id) on delete cascade,
  session_player_id uuid references public.session_players(id) on delete cascade,
  role_template_id bigint references public.role_templates(id) on delete restrict,
  content_item_id bigint references public.content_items(id) on delete restrict,
  assignment_type text not null,
  assignment_value text not null,
  hidden_metadata_json jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint session_assignments_type check (
    assignment_type in ('role', 'secret_word', 'character', 'mimica_word')
  ),
  constraint session_assignments_value_not_blank check (btrim(assignment_value) <> '')
);

create index if not exists idx_game_tutorials_game
