drop policy if exists "Public can read active games" on public.games;
create policy "Public can read active games"
on public.games
for select
to anon, authenticated
using (is_active);

drop policy if exists "Public can read tutorials for active games" on public.game_tutorials;
create policy "Public can read tutorials for active games"
on public.game_tutorials
for select
to anon, authenticated
using (
  exists (
    select 1
    from public.games
    where games.id = game_tutorials.game_id
      and games.is_active
  )
);

drop policy if exists "Public can read tutorial steps for active games" on public.tutorial_steps;
create policy "Public can read tutorial steps for active games"
on public.tutorial_steps
for select
to anon, authenticated
using (
  exists (
    select 1
    from public.game_tutorials
    join public.games on games.id = game_tutorials.game_id
    where game_tutorials.id = tutorial_steps.tutorial_id
      and games.is_active
  )
);

drop policy if exists "Public can read active roles" on public.role_templates;
create policy "Public can read active roles"
on public.role_templates
for select
to anon, authenticated
using (
  is_active
  and exists (
    select 1
    from public.games
    where games.id = role_templates.game_id
      and games.is_active
  )
);

drop policy if exists "Public can read active categories" on public.categories;
create policy "Public can read active categories"
on public.categories
for select
to anon, authenticated
using (
  is_active
  and exists (
    select 1
    from public.games
    where games.id = categories.game_id
      and games.is_active
  )
);

drop policy if exists "Public can read active difficulties" on public.difficulties;
create policy "Public can read active difficulties"
on public.difficulties
for select
to anon, authenticated
using (
  is_active
  and exists (
    select 1
    from public.games
    where games.id = difficulties.game_id
      and games.is_active
  )
);

drop policy if exists "Public can read active content" on public.content_items;
create policy "Public can read active content"
on public.content_items
for select
to anon, authenticated
using (
  is_active
  and exists (
    select 1
    from public.games
    where games.id = content_items.game_id
      and games.is_active
  )
  and (
    category_id is null
    or exists (
      select 1
      from public.categories
      where categories.id = content_items.category_id
        and categories.game_id = content_items.game_id
        and categories.is_active
    )
  )
  and (
    difficulty_id is null
    or exists (
      select 1
      from public.difficulties
      where difficulties.id = content_items.difficulty_id
        and difficulties.game_id = content_items.game_id
        and difficulties.is_active
    )
  )
);

-- No public policies are intentionally created for sessions, players or
